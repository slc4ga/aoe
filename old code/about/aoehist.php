<h2>Alpha Omega Epsilon History</h2>

<hr>

<p> 
    &nbsp;&nbsp;&nbsp;&nbsp;
    Alpha Omega Epsilon is a professional and social sorority composed of females who have majored in engineering or technical 
    sciences. In the early 1980s there was a national push to interest women in engineering. The female engineering students at 
    <span style="color:#0088cc"><b><em>Marquette University</em></b></span>, including Little Sisters of both Sigma Phi Delta and 
    Triangle Fraternities, started meeting in hopes of forming an organization to increase the number of women in engineering. They 
    decided to form a Sorority, and Alpha Omega Epsilon was founded on <span style="color:#0088cc"><b><em>November 13, 1983</em>
    </b></span>, making A&Omega;E the oldest engineering sorority. Four months later, on March 22, 1984, the Associated Students of 
    Marquette University voted on and approved the constitution, and Alpha Omega Epsilon became a recognized student organization 
    on Marquette University's campus. As a recognized local sorority, Alpha Omega Epsilon was represented at the 1984-85 
    Panhellenic Conference winning the Presidential office and attaining representatives. The first Alpha Chapter candidate class 
    was initiated in the Fall of 1984 which nearly doubled its membership from the <span style="color:#0088cc"><b><em>twenty-
    seven founding members</em></b></span>, who are listed below.
</p>
<br>
<p>
    &nbsp;&nbsp;&nbsp;&nbsp;
    Initially founded to unite <span style="color:#0088cc"><b><em>female engineers</em></b></span> of all curricula, the 
    Organization's mission has since expanded to include females in both engineering and technical sciences. The Sorority's 
    mission, ideals, and objectives have spread to other campuses, and as a result, we now have <span style="color:#0088cc"><b>
    <em>twenty-one active chapters</em></b></span>. The Sisters of Alpha Omega Epsilon promote ideals and objectives that hope 
    to further the advancement of female engineers and technical scientists, while at the same time developing bonds of lifelong 
    friendships. Alpha Omega Epsilon strives for scholarship and academic achievement. The sisters create a friendly, warm 
    atmosphere where integrity, character, and self-confidence can flourish. The Sorority hopes to enlighten female engineers and 
    technical scientists to the career opportunities available to them. Alpha Omega Epsilon encourages ties with other Greek and 
    Engineering Organizations, fosters leadership at the Chapter and International level, and provides networking opportunities for 
    its members. As a young Organization, Alpha Omega Epsilon allows each member to play a significant part in realizing the 
    Sorority's future goals.
</p>
<br>
<p>
    Alpha Omega Epsilon celebrated its silver anniversary and 
    <span style="color:#0088cc"><b><em>25 years of success on November 13, 2008</em></b></span>.
</p>

