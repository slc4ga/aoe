<h2>Ideals</h2>

<hr>

<ul> 
    <li>As an <span style="color:#0088cc"><b><em>individual</em></b></span>, I will promote integrity, character, 
        honesty, self-confidence, and uphold my financial responsibilities.</li>
    <li>As a <span style="color:#0088cc"><b><em>Sorority</em></b></span>, we will promote unity, friendship, 
        professionalism, follow traditions, and maintain our image.</li>
    <li>As an <span style="color:#0088cc"><b><em>integral part of the school</em></b></span>, we will promote 
        engineering and technical science activities, high scholastic standards, and strong relations between 
        students and faculty.</li>
    <li>As an <span style="color:#0088cc"><b><em>active member in the community</em></b></span>, we will promote understanding, 
        knowledge, and communication within our profession.</li>
</ul>
<br>
<br>
<h2>Objectives</h2>
<hr>

<ul>
    <li>To create friendships while promoting self-confidence, professionalism and motivation among female engineers 
        and technical scientists of all curricula.</li>
    <li>To work for the betterment of the individual and the Sorority as a whole.</li>
    <li>To create a friendly and warm environment among its members. </li>
    <li>To promote the welfare and interest of the profession. </li>
    <li>To strengthen the relationships between students and faculty. </li> 
    <li>To enhance educational achievements and scholarship. </li>
    <li>To help develop the highest standards of personal integrity and character. </li>
    <li>To foster and maintain the bond of sisterhood through a continuing program for the alumnae. </li>
</ul>