<h2>Mission Statement</h2>

<hr>

<p> 
    &nbsp;&nbsp;&nbsp;&nbsp;
    Alpha Omega Epsilon is a professional and social sorority for women in engineering and technical science majors founded in 1983 
    that promotes friendship, leadership, and professionalism to all members of the organization, the community, and our 
    professions.
</p>
<br>

<h3>Vision Statement</h3>


<p>
    &nbsp;&nbsp;&nbsp;&nbsp;
    Alpha Omega Epsilon is the spirit of women in engineering and the technical sciences, devoted to an enriching and life-changing 
    experience for our members. Sisters commit to a lifelong engagement with Alpha Omega Epsilon by reinforcing the bonds between 
    each other and enhancing the experiences of future generations of engineers and technical scientists.
</p>
<p>
    &nbsp;&nbsp;&nbsp;&nbsp;
    The Sorority grows by developing successful members and instilling our core values in our membership. Our recognized and 
    trusted brand represents the well-rounded engineer and technical scientist, inspiring pride in our Sisters. Our infrastructure, 
    business processes, and expansion efforts provide a sustained competitive advantage. And, our network of diverse and talented 
    volunteers are aligned in the relentless pursuit of our common purpose.
</p>	 